xl, yl = np.genfromtxt(dir+'/source/AST_Data.txt',delimiter='',unpack=True) 
xf, yf = np.genfromtxt(dir+'/source/AST_Data_Normalised.txt',delimiter='',unpack=True) 
plt.figure()
