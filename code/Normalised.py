import os,  numpy as np, matplotlib.pyplot as plt

dir = os.path.abspath(os.path.join(os.path.dirname(__file__), '..'))

xa, ya = np.genfromtxt(dir+'/source/AST_Data_Normalised.txt',delimiter='',unpack=True) 
plt.figure()
plt.plot(xa,ya) 
plt.xlabel('Pixel Value')
plt.ylabel('Pixel ADUs')
plt.title('AST Normalised Spectrum Data Graph')
plt.savefig(dir+'/results/AST_Normalised_Graph.png', dpi=300, bbox_inches='tight')

xb, yb = np.genfromtxt(dir+'/source/SA_Data_Normalised.txt',delimiter='',unpack=True) 
